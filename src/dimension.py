import pandas as pd
from config import SENTIMENT_OUT, DIMENSION_OUT

# ================================
# 1. 读取数据
# ================================
df = pd.read_csv(SENTIMENT_OUT)

# ================================
# 2. Topic → 维度 映射
# 注意：每个 Topic ID 只能出现一次
# ================================
topic_to_dimension = {}

mapping = {
    '促销与商业策略感知':      [0,8,16,20,26,37,46,53,62,63,65,80,83,84,91,100,107,111,133,136,140,154,159,163,165,169],
    '系统性能与运行稳定性':    [4,21,33,35,54,60,66,82,94,99,137,142,144,145,162],
    '功能丰富度与实用性':      [1,3,32,36,69,85,123,135,157,164],
    '生成内容质量':            [2,11,12,13,14,23,25,30,49,55,67,68,76,86,87,90,92,96,102,108,109,129,132,134,138,153,155],
    '整体可用性':              [27,61,119,120,121],
    '情感交互':                [40,47,48,59,64,74,77,93,101,122,131,149,150],
    '使用约束与内容管控':      [10,78],
    '模型表现变化感知':        [7,42,114],
    '整体满意度与情绪化表达':  [5,15,19,22,31,34,41,58,70,73,75,81,95,97,98,103,104,110,112,115,116,117,124,125,126,127,128,130,139,141,143,146,148,156,158,160,166,167,168,170,171],
    '平台偏好':                [28,71,72,79,151,152,172],
}

# 检查重复 Topic ID
all_ids = [tid for ids in mapping.values() for tid in ids]
duplicates = [tid for tid in all_ids if all_ids.count(tid) > 1]
if duplicates:
    print(f"⚠️  警告：以下 Topic ID 存在重复映射，请核查：{set(duplicates)}")

for dimension, topic_ids in mapping.items():
    for tid in topic_ids:
        topic_to_dimension[tid] = dimension

# ================================
# 3. 映射 + 填充未分类
# ================================
df['维度'] = df['Topic'].map(topic_to_dimension).fillna('未分类')

# ================================
# 4. 统计
# ================================
print("\n=== 各维度数量 ===")
print(df['维度'].value_counts())
print(f"\n未分类数量：{(df['维度'] == '未分类').sum()}")

# ================================
# 5. 输出
# ================================
df.to_csv(DIMENSION_OUT, index=False, encoding='utf-8-sig')
print("完成！输出：", DIMENSION_OUT)